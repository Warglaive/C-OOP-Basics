public class Engine
{
    private string model;
    private string power;
    private string displacement;
    private string efficiency;

    public string Model
    {
        get => model;
        set => model = value;
    }

    public string Power
    {
        get => power;
        set => power = value;
    }

    public string Displacement
    {
        get => displacement;
        set => displacement = value;
    }

    public string Efficiency
    {
        get => efficiency;
        set => efficiency = value;
    }
}
